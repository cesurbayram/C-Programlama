#include<stdio.h>
#include<stdlib.h>
#include<math.h>

int main()
{
        /* ceil floor sqrt pow abs*/

        printf("%f\n",ceil(4.3));//tavana yuvarlama alır
        printf("%f\n",floor(4.9));//tabana yuvarlama alır
        printf("%f\n",sqrt(81));//karekökünü alır
        printf("%f\n",pow(3,3));//üslü ifade alır 
        printf("%d\n",abs(-13));//pozitif değeri alır

    return 0;
}